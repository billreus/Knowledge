package util;

import java.awt.Color;
import java.awt.Dimension;
import java.io.File;

import javax.swing.ImageIcon;
import javax.swing.JButton;
import javax.swing.JComponent;
import javax.swing.JFrame;
import javax.swing.JOptionPane;
import javax.swing.JPanel;
import javax.swing.JTextField;
/**
 * 图像界面工具用于一些UI的设置
 */
public class GUIUtil {
	private static String imageFolder = "D:/CS/Knowledge/JAVA/JAVA记账/daybook-project-master/daybook-project-master/daybook/img";
	/*
	 * 给按钮设置图标和文本以及提示文字
	 */
	public static void setImageIcon(JButton b, String fileName, String tip) {
		ImageIcon i = new ImageIcon(new File(imageFolder, fileName).getAbsolutePath());
		b.setIcon(i);
		b.setPreferredSize(new Dimension(61, 81));
		b.setToolTipText(tip);
		b.setVerticalTextPosition(JButton.BOTTOM);
		b.setHorizontalTextPosition(JButton.CENTER);
		b.setText(tip);
	}
	/*
	 * 给多个组件设置前景色
	 */
	public static void setColor(Color color, JComponent... cs) {
		for (JComponent c : cs) {
			c.setForeground(color);
		}
	}

	/*
	 * 开始显示一个面板的内容--方便显示面板，用于面板的测试
	 *  拉伸比例1表示满屏幕
	 */
	public static void showPanel(JPanel p, double strechRate) {
		GUIUtil.useLNF();  // 使用水晶皮肤
		JFrame f = new JFrame();
		f.setSize(500, 500);
		f.setLocationRelativeTo(null);
		CenterPanel cp = new CenterPanel(strechRate);
		f.setContentPane(cp);
		f.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
		f.setVisible(true);
		cp.show(p);
	}

	public static void showPanel(JPanel p) {
		showPanel(p, 0.85);
	}
	/*
	 * 校验一个组件内容是否是数字格式
	 */
	public static boolean checkNumber(JTextField tf, String input) {
		if (!checkEmpty(tf, input))
			return false;
		String text = tf.getText().trim();
		try {
			Integer.parseInt(text);
			return true;
		} catch (NumberFormatException e1) {
			JOptionPane.showMessageDialog(null, input + " 需要是整数");
			tf.grabFocus();
			return false;
		}
	}
	/*
	 * 校验一个组件内容是否为0
	 */
	public static boolean checkZero(JTextField tf, String input) {
		if (!checkNumber(tf, input))
			return false;
		String text = tf.getText().trim();

		if (0 == Integer.parseInt(text)) {
			JOptionPane.showMessageDialog(null, input + " 不能为零");
			tf.grabFocus();
			return false;
		}
		return true;
	}

	/*
	 * 校验一个输入框内容是否为空
	 */
	public static boolean checkEmpty(JTextField tf, String input) {
		String text = tf.getText().trim();
		if (0 == text.length()) { // 通过text长度判断，内容是否为空
			// 对话框提示用户
			JOptionPane.showMessageDialog(null, input + " 不能为空");
			tf.grabFocus(); // 输入框获取焦点
			return false;
		}
		return true;

	}

	public static int getInt(JTextField tf) {
		return Integer.parseInt(tf.getText());
	}
	/*
	 * 使用水晶皮肤
	 * 需要将jar包放到lib目录下，导入到项目中
	 */
	public static void useLNF() {
		try {
			javax.swing.UIManager.setLookAndFeel("com.birosoft.liquid.LiquidLookAndFeel");
		} catch (Exception e) {
			e.printStackTrace();
		}
	}
}